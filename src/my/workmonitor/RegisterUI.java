    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here: 
        String nombre=jTextField1.getText().toString();
        String apellido=jTextField2.getText().toString();
        String usuario=jTextField3.getText().toString();
        String password=jPasswordField4.getText().toString();
        
        if(nombre.equals("")){
            JOptionPane.showMessageDialog(null, "Debe ingresar su nombre");
            return;
        }
        if(apellido.equals("")){
            JOptionPane.showMessageDialog(null, "Debe ingresar su apellido");        
            return;
        }
        if(usuario.equals("")){
            JOptionPane.showMessageDialog(null, "Debe ingresar su usuario");        
            return;
        }
        if(password.equals("")){
            JOptionPane.showMessageDialog(null, "Debe ingresar su password");   
            return;
        }
        
        PasswordHelper passwordHelper=new PasswordHelper();
        
        Persona persona=new Persona();
        persona.setId(persona.getId());
        persona.setNombre(nombre.trim());
        persona.setApellido(apellido.trim());
        persona.setUsuario(usuario.trim());
        persona.setPassword(passwordHelper.encriptarMD5base64(password.trim()));                        
                
        personaDao.getAll(Persona.class);
        
        List<Persona> personas=personaDao.getByNombreApellido(nombre, apellido);
        
        if(!personas.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ya existe una persona con este nombre y este apellido"); 
            jTextField1.setText(""); 
            jTextField2.setText("");
            jTextField3.setText("");
            jPasswordField4.setText("");            
            return;
        }                                
                 
        try {
            if(personaDao.save(persona)==(Serializable)0){
                JOptionPane.showMessageDialog(null, "Error al ingresar usuario. No se ha podido ingresar");
                jTextField1.setText("");
                jTextField2.setText("");
                jTextField3.setText("");
                jPasswordField4.setText("");
                return;        
            }
        } catch (Exception ex) {
            Logger.getLogger(RegisterUI.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        JOptionPane.showMessageDialog(null, "El usuario se ha ingresado correctamente");   
        this.dispose();
        return;           
        
    }//GEN-LAST:event_jButton1ActionPerformed

